package org.arpico.groupit.receipt.dto;

public class HierarchyTransferDto {
	
	private String masterSalesC;
	private String name;
	private String cls;
	private String from;
	private String to;
	
	public String getMasterSalesC() {
		return masterSalesC;
	}
	public void setMasterSalesC(String masterSalesC) {
		this.masterSalesC = masterSalesC;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCls() {
		return cls;
	}
	public void setCls(String cls) {
		this.cls = cls;
	}
	public String getFrom() {
		return from;
	}
	public void setFrom(String from) {
		this.from = from;
	}
	public String getTo() {
		return to;
	}
	public void setTo(String to) {
		this.to = to;
	}
	
	

}
