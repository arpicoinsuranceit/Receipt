package org.arpico.groupit.receipt.dto;

public class ShortPremiumDto {

	private Integer quonum;
	private String pprnum;
	private Integer prpseq;
	private String agent;
	private String loccod;
	private String addnot;
	private Integer count;

	public Integer getQuonum() {
		return quonum;
	}

	public void setQuonum(Integer quonum) {
		this.quonum = quonum;
	}

	public String getPprnum() {
		return pprnum;
	}

	public void setPprnum(String pprnum) {
		this.pprnum = pprnum;
	}

	public Integer getPrpseq() {
		return prpseq;
	}

	public void setPrpseq(Integer prpseq) {
		this.prpseq = prpseq;
	}

	public String getAgent() {
		return agent;
	}

	public void setAgent(String agent) {
		this.agent = agent;
	}

	public String getLoccod() {
		return loccod;
	}

	public void setLoccod(String loccod) {
		this.loccod = loccod;
	}

	public String getAddnot() {
		return addnot;
	}

	public void setAddnot(String addnot) {
		this.addnot = addnot;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}

}
